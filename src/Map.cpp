Map::Map()
{
	grass = TextureManager::loadTexture("grass.png");
	grass_1 = TextureManager::loadTexture("grass_1.png");
	puddle = TextureManager::loadTexture("puddle.png");
	LoadMap(lvl1);
	src.x = src.y = 0;
	src.h = 32;
	src.w = 32;
	dest.h = 32;
	dest.w = 32;
	dest.x = 0;
	dest.y = 0;
}
Map::~Map()
{

}
void Map::DrawMap()
{
	int type = 0;

	for (int row = 0; row < 23; row++)
	{
		for (int column = 0;column < 28;column++)
		{
			type = map[row][column];
			dest.x = column * 32;
			dest.y = row * 32;
			switch (type)
			{
			case 0:
				TextureManager::Draw(grass_1, src, dest);
				break;
			case 1:
				TextureManager::Draw(grass, src, dest);
				break;
			case 2:
				TextureManager::Draw(puddle, src, dest);
			default:
				break;
			}
		}
	}
}
void Map::LoadMap(std::vector<std::vector<int>> arr)
{
	map.resize(23);
	for (int row = 0; row < 23;row++)
	{
		map[row].resize(28);
	}
	for (int row = 0; row < 23; row++)
	{
		for (int column = 0;column < 28;column++)
		{
			map[row].push_back(arr[row][column]);
		}
	}
}